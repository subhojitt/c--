#include <iostream>
using namespace std;
int main(){
        
        //int datatype
        int apples=5;
        cout<<"apples"<<endl;
        cout<<"the size of int: "<<sizeof(apples)<<endl;


        //character datatype
        char human   ='sam';
        cout<<"human"<<endl;
        cout<<"the size of char: "<<sizeof(human)<<endl;

        //boolean datatype
        bool flag= false;
        cout<<"flag"<<endl;
        cout<<"the size of bool: "<<sizeof(flag)<<endl;
         

         //floating datatype
        float marks=77.98 ;
        cout<<"marks"<<endl;
        cout<<"the size of float: "<<sizeof(marks)<<endl;



        return 0;

}